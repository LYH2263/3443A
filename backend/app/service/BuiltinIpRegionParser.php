    public function parse(string $ip): array
    {
        if ($this->isInternalIp($ip)) {
            return ['province' => '未知', 'city' => '未知'];
        }

        if (filter_var($ip, FILTER_VALIDATE_IP, FILTER_FLAG_IPV6)) {
            return $this->parseIpv6($ip);
        }

        $province = $this->lookupIpv4($ip);
        return ['province' => $province, 'city' => $province === '未知' ? '未知' : $province];
    }

    private function isInternalIp(string $ip): bool
    {
        if (empty($ip) || $ip === '0.0.0.0' || $ip === '::' || $ip === '::1') {
            return true;
        }

        if (filter_var($ip, FILTER_VALIDATE_IP, FILTER_FLAG_IPV6)) {
            $lower = strtolower($ip);
            if (str_starts_with($lower, 'fc') || str_starts_with($lower, 'fd') ||
                str_starts_with($lower, 'fe80:') || $lower === '::1' ||
                str_starts_with($lower, 'ff') || str_starts_with($lower, 'fe')) {
                return true;
            }
            $mapped = $this->extractIpv4FromIpv6($ip);
            if ($mapped) {
                return $this->isInternalIp($mapped);
            }
            return false;
        }

        $long = ip2long($ip);
        if ($long === false) return true;
        if ($long < 0) $long += 4294967296;

        $privateRanges = [
            [167772160, 184549375],
            [2886729728, 2887778303],
            [3232235520, 3232301055],
            [2130706432, 2147483647],
            [0, 16777215],
            [2851995648, 2852061183],
        ];

        foreach ($privateRanges as $range) {
            if ($long >= $range[0] && $long <= $range[1]) {
                return true;
            }
        }

        return false;
    }

    private function extractIpv4FromIpv6(string $ip): ?string
    {
        if (preg_match('/::ffff:(\d+\.\d+\.\d+\.\d+)$/i', $ip, $m)) {
            return $m[1];
        }
        if (preg_match('/:(\d+\.\d+\.\d+\.\d+)$/i', $ip, $m)) {
            return $m[1];
        }
        return null;
    }

    private function parseIpv6(string $ip): array
    {
        $mapped = $this->extractIpv4FromIpv6($ip);
        if ($mapped) {
            $province = $this->lookupIpv4($mapped);
            return ['province' => $province, 'city' => $province === '未知' ? '未知' : $province];
        }
        return ['province' => '未知', 'city' => '未知'];
    }

    private function lookupIpv4(string $ip): string
    {
        $parts = explode('.', $ip);
        if (count($parts) !== 4) return '未知';

        $prefixes = [
            $parts[0] . '.' . $parts[1] . '.' . $parts[2],
            $parts[0] . '.' . $parts[1],
        ];

        foreach ($prefixes as $prefix) {
            if (isset(self::$provincePrefixes[$prefix])) {
                return self::$provincePrefixes[$prefix];
            }
        }

        return '未知';
    }
